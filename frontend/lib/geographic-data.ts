export const getComunidadesAutonomas = () => Object.keys(geographicData).sort();

export const getProvincias = (comunidad: string) => {
  if (!geographicData[comunidad as keyof typeof geographicData]) return [];
  return Object.keys(geographicData[comunidad as keyof typeof geographicData]).sort();
};

export const getMunicipios = (comunidad: string, provincia: string) => {
  const ccaa = geographicData[comunidad as keyof typeof geographicData];
  if (!ccaa) return [];
  const prv = ccaa[provincia as keyof typeof ccaa] as string[] | undefined;
  if (!prv) return [];
  return prv.sort();
};
